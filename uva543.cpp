bool is_prime(ll n)
{
    if (n <= 1)
        return 0;
    for (ll i = 2; i * i <= n; i++)
        if (n % i == 0)
            return 0;
    return 1;
}

void solve()
{
    ll n;
    while (cin >> n)
    {
        if (n == 0)
            break;
        ll x = n, f = 1;
        if (x % 2 == 0)
            x--;
        for (int i = 3; i <= x;)
        {
            if (!is_prime(i))
                i += 2;
            if (!is_prime(x))
                x -= 2;
            if (is_prime(i) && is_prime(x))
            {
                if (x + i == n)
                {
                    cout << n << " = " << i << " + " << x << endl;
                    f = 0;
                    break;
                }
                else if (x + i > n)
                    x -= 2;
                else
                    i += 2;
            }
        }
        if (f)
            cout << "Goldbach's conjecture is wrong.\n";
    }
}
